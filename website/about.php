    <h2>Nous sommes un collectif autogéré intégré au STRIP (Syndicat Trans
des Injecteurices Précarisé-e-s) formé début 2023, par des personnes
concernées et militantes.<br>
Notre but est d'offrir aux personnes concernées trans, enby, agenres
ou en questionnement <b>un espace de sociabilité en non-mixité</b>, safe
et sans jugement pour s'exprimer, se renseigner ou partager la
compagnie d'autres personnes concernées.<br>
Nous proposons chaque dimanche une soirée accompagnée d'un
atelier de réduction des risques pour les personnes trans dans leurs
multitudes et dans le respect de leurs identités propres.<br><br>
Vous pouvez nous retrouver tous les dimanches à 18h pour notre
rassemblement hebdomadaire ouvert à toutes les personnes trans,
enby, agenres ou en questionnement si vous avez des questions sur
votre parcours, votre identité, votre santé ou les moyens de
s'hormoner, pour discuter ou partager à boire et à manger!<br><br>
Dans tous les cas, vous pouvez financer nos transitions ! Faites un
don, cet argent nous permet de continuer nos activités de réduction
des risques et de soutenir les personnes trans ou en questionnement
de Reims et de ses alentours.<br><br>
Autonomie trans et adelphite!<br><br>
